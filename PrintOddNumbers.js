//use for loop
console.log("----- For loop ------");
for(let i= 1 ; i<=10 ; i++)
{
	console.log(i);
}

//print reverse numbers
console.log("----- Print reverse numbers ------");
for(let i= 10 ; i>0 ; i--)
{
	console.log(i);
}

//print odd Number
console.log("----- Print odd numbers ------");
for(let j= 1 ; j<=10 ; j++)
{
	if(j%2==1)
	{
		console.log(j);
	}
}